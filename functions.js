// FUNCIONES

// Dos tipos de funciones
// 1. Function: DECLARATIVA
function miFuncion() {
    return 3;
}
miFuncion();


// 2. Function: DE EXPRESION
// hay una funcion dentro de la variable, 
var miFuncion = function(a,b) {
    return a + b;
}

// Una funcion necesita parametros que seran utilizados para trabajar con las funciones

// mandamos llamar la funcion declarativa
miFuncion();
